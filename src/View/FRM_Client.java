    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        if(fila>-1){
            int res = JOptionPane.showConfirmDialog(this, "Realmente desea eliminar el registro?");
            if (res == 0) {
                cc.EliminarC(Integer.parseInt(String.valueOf(Tabla.getValueAt(fila, 0))));
                ListarTBL("");
                JOptionPane.showMessageDialog(this, "Registro eliminado", "Advertencia", JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
        JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UNA FILA","Advertencia",JOptionPane.OK_OPTION+JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_EliminarActionPerformed

