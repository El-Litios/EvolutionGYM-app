    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        if (Rut.getText().equals("") || Nom.getText().equals("") || Ap.getText().equals("") || Am.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos", "EvolutionGym",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
        }else{
            if (sw==0) cc.AgregarC(Rut.getText(), Nom.getText(), Ap.getText(), Am.getText());
            else cc.ModificarC(cod, Rut.getText(), Nom.getText(), Ap.getText(), Am.getText());
        }
        Rut.setText("");
        Nom.setText("");
        Ap.setText("");
        Am.setText("");
        sw=0;
        JOptionPane.showMessageDialog(this, "OPERACION REALIZADA", "EvolutionGym",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
        ListarTBL("");
        Panel.setSelectedIndex(0);
    }//GEN-LAST:event_GuardarActionPerformed

