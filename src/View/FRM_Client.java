    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
        if (fila>-1) {
            cod=Integer.parseInt(String.valueOf(Tabla.getValueAt(fila, 0)));
            Rut.setText(String.valueOf(Tabla.getValueAt(fila, 1)));
            Nom.setText(String.valueOf(Tabla.getValueAt(fila, 2)));
            Ap.setText(String.valueOf(Tabla.getValueAt(fila, 3)));
            Am.setText(String.valueOf(Tabla.getValueAt(fila, 4)));
            sw=1;
            Panel.setSelectedIndex(1);
        } else {
           JOptionPane.showMessageDialog(this, "Debe seleccionar una fila","ADVERTENCIA",JOptionPane.OK_OPTION+JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_ModificarActionPerformed

